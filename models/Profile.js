const mongoose = require('mongoose')

const { Schema } = mongoose

const ProfileSchema = new Schema({
  // Associating profile with the user Schema by ID
  // We want information about user in profile
  user: {
    type: Schema.Types.ObjectId,
    ref: 'users', // reference to users Model
  },
  // User friendly URL, eg. /profiles/john-doe
  // which users will be able to change
  handle: {
    type: String,
    required: true,
    max: 40, // number of allowed characters
  },
  company: {
    type: String,
  },
  website: {
    type: String,
  },
  location: {
    type: String,
  },
  status: {
    type: String,
    required: true,
  },
  skills: {
    type: [String], // Array of strings
    required: true,
  },
  bio: {
    type: String,
  },
  githubusername: {
    type: String,
  },
  experience: [
    {
      title: {
        type: String,
        required: true,
      },
      company: {
        type: String,
        required: true,
      },
      location: {
        type: String,
      },
      from: {
        type: Date,
        required: true,
      },
      to: {
        type: Date,
      },
      current: {
        type: Boolean,
        default: false,
      },
      description: {
        type: String,
      },
    },
  ],
  education: [
    {
      school: {
        type: String,
        required: true,
      },
      degree: {
        type: String,
        required: true,
      },
      fieldofstudy: {
        type: String,
        required: true,
      },
      from: {
        type: Date,
        required: true,
      },
      to: {
        type: Date,
      },
      current: {
        type: Boolean,
        default: false,
      },
      description: {
        type: String,
      },
    },
  ],
  social: {
    youtube: {
      type: String,
    },
    twitter: {
      type: String,
    },
    facebook: {
      type: String,
    },
    linkedin: {
      type: String,
    },
    instagram: {
      type: String,
    },
    date: {
      type: Date,
      default: Date.now,
    },
  },
})

const Profile = mongoose.model('profile', ProfileSchema)

module.exports = Profile
